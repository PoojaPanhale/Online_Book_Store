package classandobject;
//constructor is special type of method
public class Demo {
	int i=90;
	public Demo()// default constructor no parameter are passed
	
	{
		System.out.println(i);
	}
	public Demo (int i)//parameter constructor 
	{
		System.out.println(i);
	}
	public void display()
	{
		System.out.println(i);
	}

}
